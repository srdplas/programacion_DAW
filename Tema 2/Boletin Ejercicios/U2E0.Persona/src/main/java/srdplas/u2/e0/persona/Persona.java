public class Persona {
    public int edad;
    public String nombre;

    
    /** 
    @return edad
    **/
    public int getEdad() {
        return edad;
    }
    /**
     * 
     * @param edad 
     */
    public void setEdad(int edad) {
        this.edad = edad;
    }
    /**
     * 
     * @return nombre
     */
    public String getNombre() {
        return nombre;
    }
    /**
     * 
     * @param nombre 
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
