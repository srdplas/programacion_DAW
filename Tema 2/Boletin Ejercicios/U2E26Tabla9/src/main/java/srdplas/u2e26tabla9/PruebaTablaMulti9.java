public class PruebaTablaMulti9 {
    public static void main(String[] args) {
        int contador=0;
        while(contador<=10){
            int resultado=contador*9;
            System.out.println(contador+" x "+ 9 +" = "+ resultado);
            contador++;
        }
    }
}
