
package srdplas.u2.e4.persona;

/**
 *
 * @author srdpl
 */
public class PersonaCondicional {
    public int edad, altura, peso;
    public boolean estaCasado;
    //Propiedades

    
    
    /**
     * 
     * @return edad
     */
    public int getEdad() {
        return edad;
    }
    /**
     * 
     * @param edad 
     */
    public void setEdad(int edad) {
        this.edad = edad;
    }
    /**
     * 
     * @return altura
     */
    public int getAltura() {
        return altura;
    }
    /**
     * 
     * @param altura 
     */
    public void setAltura(int altura) {
        this.altura = altura;
    }
    /**
     * 
     * @return peso
     */
    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }
    /**
     * 
     * @return estaCasado
     */
    public boolean isEstaCasado() {
        return estaCasado;
    }
    /**
     * 
     * @param estaCasado 
     */
    public void setEstaCasado(boolean estaCasado) {
        this.estaCasado = estaCasado;
    }
     
}
